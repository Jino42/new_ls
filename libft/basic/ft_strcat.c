char	*ft_strcat(char *dest, const char *src)
{
	int i;
	int n;

	n = 0;
	i = 0;
	while (dest[i])
		i++;
	while (src[n])
	{
		dest[i + n] = src[n];
		n++;
	}
	dest[i + n] = '\0';
	return (dest);
}
